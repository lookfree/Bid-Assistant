// 投标助手输入预判（2026-07-22 实测问题）：右栏助手是「改写执行器」，任何输入都会发起
// 计费改写。用户把它当问答机器人（如问「你改了哪里?」）→ 白扣 25 积分还把正文重写一遍。
// 这里在发起计费前拦截**明显是提问/闲聊**的输入，本地免费回复引导；判不准时放行（宁可执行，
// 不误拦真指令——改写有撤销兜底 + 服务端非 HTML 拒收退款兜底，误拦指令则功能直接失灵）。
//
// 审查修正（同日）：① 提问判定必须先于指令动词判定——「你把哪里改了?」含「把」，动词先测会绕过；
// ② 只拦短输入（≤20 字）——长句里的「有什么区别」多半是写作指令的一部分（如「详细说明…有什么区别」）。

const MAX_QUESTION_LEN = 20

// 明显的"问助手"句式（仅对短输入判定）
const QUESTION_PATTERNS = [
  /改了(哪里|什么|啥)/, // 你改了哪里 / 你把哪里改了(→ 哪里改了)
  /(哪里|什么|啥)(被)?改了/,
  /^你(是谁|能做什么|会什么|怎么用)/,
  /^(为什么|为啥|怎么回事)/,
  /什么(用|功能|区别)$/,
]
// 出现这些动词大概率是真改写指令，带问号也放行（如"能不能把响应时间改成15分钟?"）。
// 注意不含裸「把/将」——提问也常带（"你把哪里改了"），单靠它判指令会漏拦。
const INSTRUCTION_VERBS = /改成|改为|修改为|调整|扩写|缩写|补充|删除|删掉|增加|加上|润色|重写|替换|更正式|提炼|优化/

/** 是否应拦截为"提问"（不发起计费改写）。只对 ≤20 字的短输入判定，长输入一律放行。 */
export function isQuestionNotInstruction(text: string): boolean {
  const t = text.trim()
  if (!t || t.length > MAX_QUESTION_LEN) return false
  if (QUESTION_PATTERNS.some((p) => p.test(t))) return true // 提问句式优先，防「把/改」类字眼误判成指令
  if (INSTRUCTION_VERBS.test(t)) return false
  return /[?？]$/.test(t) // 短句以问号结尾且无指令动词 → 按提问处理
}

