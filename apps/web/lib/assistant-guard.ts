// 投标助手输入预判（2026-07-22 实测问题）：右栏助手是「改写执行器」，任何输入都会发起
// 计费改写。用户把它当问答机器人（如问「你改了哪里?」）→ 白扣 25 积分还把正文重写一遍。
// 这里在发起计费前拦截**明显是提问/闲聊**的输入，本地免费回复引导；判不准时放行（宁可执行，
// 不误拦真指令——改写有撤销兜底，误拦指令则功能直接失灵）。

// 明显的"问助手"句式：改了什么/你是谁/能做什么/为什么…（含结尾问号变体）
const QUESTION_PATTERNS = [
  /改了(哪里|什么|啥)/,
  /(哪里|什么|啥)(被)?改/,
  /你(是谁|能做什么|会什么|怎么用)/,
  /^(为什么|为啥|怎么回事)/,
  /(有什么|什么)(用|功能|区别)/,
]
// 出现这些动词大概率是真改写指令，即使带问号也放行（如"能不能把响应时间改成15分钟?"）
const INSTRUCTION_VERBS = /[把将]|改成|改为|修改|调整|扩写|缩写|补充|删除|删掉|增加|加上|润色|重写|替换|更正式|提炼|优化/

/** 是否应拦截为"提问"（不发起计费改写）。 */
export function isQuestionNotInstruction(text: string): boolean {
  const t = text.trim()
  if (!t) return false
  if (INSTRUCTION_VERBS.test(t)) return false
  if (QUESTION_PATTERNS.some((p) => p.test(t))) return true
  // 短句 + 以问号结尾 + 无指令动词：按提问处理（长问号句多半是带问法的指令，放行）
  return /[?？]$/.test(t) && t.length <= 20
}

/** 拦截后的本地引导话术（不消耗积分）。 */
export const QUESTION_GUIDE_REPLY =
  "我是改写执行器：把要求写成指令我就直接改正文，例如「把响应时间改为15分钟」「本章更正式一些」。" +
  "想看上次改了哪里，可在编辑器用撤销按钮对照修改前后。本条回复不消耗积分。"
